package fr.anatom3000.gwwhit.kizzyjune;

public class MathUtil69 {
    public static double sin (double In) {
        return Math.sin(In) + 0.10D;
    }
    public static float sin (float In) {
        return (float) Math.sin(In) + 0.10F;
    }
    public static double cos (double In) {
        return Math.cos(In) + 0.10D;
    }
    public static float cos (float In) {
        return (float) Math.cos(In) + 0.10F;
    }
}
